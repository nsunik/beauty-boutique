<li>Hair loss could be caused due to several reasons such as lack of vitamins, minerals or iron in the body, age, pollution, heredity, stress, lack of sleep, hormonal imbalance and illness. </li>
<h2>In Women:</h2>
    <li>Hair Loss in women is attributed to genetic predisposition, aging, hormonal imbalance and the levels of endocrine hormones, particularly androgens. During the time that hormonal changes caused by menopause take place, the levels of androgens rise and many women find that their hair has thinned. <li>
	<h2>In Men</h2>
<li> Hereditary hair loss in men is due to the action of dihydrotestosterone (DHT) on scalp hair follicles. The hair follicles react to dihydrotestosterone making them shrink and balding gradually takes place.</li>
