<h2>Temporary Tattoo</h2>
    <li> Temporary Tattoo is a non-permanent image on the skin resembling a permanent tattoo. As a form of body painting, temporary tattoos can be drawn, painted, airbrushed, or needled in the same way as permanent tattoos, but with an ink which dissolves in the blood within 6 months. <li>
	<h2>Henna Tattoo</h2>
<li>
A henna tattoo is a temporary body art technique that involves drawing designs on the skin using a henna-based ink.To make the ink, henna leaves are crushed into a paste or dried into a powder, then mixed with a liquid such as water, tea, or lemon juice. Honey or sugar can also be added to the mixture. The artist then pipes or brushes the henna ink onto the skin with a toothpick or stick and leaves it to dry. The drying process gives the henna time to stain the skin. </li>
<h2>3D Tattoo</h2>
<li>3D tattoo” or “3-dimensional tattoo” refer to tattoos that have an extra dimension, a third dimension. These tattoos interact with the skin, giving the illusion of being above the skin or within the body.</li>
	
